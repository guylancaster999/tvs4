<strong>Suggested activities</strong>
<ul>
<li>At the start of the partnership link, each school produces a poster on what they know about the partners’ region, village or country. Then, after researching their partner’s country etc. they produce a second poster. This allows for discussion on stereotypes and realities of initial perceptions.</li>
<li>Introductory letter or email exchange using school (not personal) addresses. Teachers identify buddy partners for their respective learners.</li>
<li>Pupils produce a PowerPoint presentation introducing themselves and their school to each other; these are then exchanged. This can be on a flash drive or CD if internet link is weak.</li>
<li>Pupils produce and exchange a weekly diary with illustrations.</li>
<li> A whole class can produce a hand book about their school and its locality.</li>
<li> Schools exchange a dozen pictures of their school /locality. Partners then annotate each picture asking key questions. The first school then writes answers and returns these to the school generating the questions. This can be done either by email or letter.</li>
<li>Mapping skills - pupils plot or draw their journey to school.</li>
<li>Both schools log the weather locally and each week send a summary to each other .</li>
<li>Pupils produce a simple Xhosa- English phrasebook (with illustrations where necessary).</li>
<li>Each partner school produces a cook book of basic local recipes.</li>
<li>Bi-lingual story exchange: learners create simple stories that can be shared with younger children in both English and Xhosa.</li>
<li>Partners produce a Guide Book to their own area and exchange them.</li>
<li>Projects can be developed that link themes from the curriculum of each school, such as: water and the water cycle; maths problem solving.</li>
</ul>
