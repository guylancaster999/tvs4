 Once your school has decided to work towards a Global School partnership, it’s important that all staff are aware and supportive of the initiative;
 it is integrated into the school Improvement/Development plan; 
 and there is more than one named member of staff involved in leading the activity. 
 It’s important that the partnership supports the school’s curriculum priorities.
<ul>
<li>It’s helpful when starting out to link with a school in your locality that already has an overseas partnership. 
They will have useful experience and advice around practicalities and know what has worked and what hasn’t. Perhaps invite a teacher with experience of linking to a staff meeting. TVS can also provide a speaker.
</li>
<li> Inform parents and involve Governors of your plans.
</li>
<li> Keep the focus of the partnership on learning, not on providing resources for the Tyume Valley school. 
(It may be appropriate for a parent or local community group to take this up this later.)
</li>
<li> If you decide to contact any of the Schools on the Tyume Valley Schools website directly please let us know so that we can support the process.
</li>
<li> Create a plan of action and consider applying for a British Council Reciprocal Visit Grant or working towards the International School Award.
</li>
<li> Partnerships go through a series of stages as they develop.
</li>
<li> Initial Contact
</li>
<li> Agreeing means and regularity of contact
</li>
<li>Identifying mutual curriculum Topics that fit with Global Education
</li>
<li> Making a formal agreement
</li>
<li> Organising possible staff visits
</li>
<li> Above all don’t let setbacks put you off. Often there are problems such a staff changes, where a lead teacher moves to another schools.
</li>
<li> If you need further advice and support contact TVS or your local Development Education Centre.
</li>
</ul>
<h3>How TVS can help</h3>

All the Tyume Valley schools interested in partnership with a UK school are registered 
on the <a href="http://www.britishcouncil.org/schools">British Council Connecting Classrooms website</a>
 and with the <a href="http://globalclassrooms.org.uk/">Development Education Centre network</a>.
<br/>
<br/>
TVS will provide administrative support for Schools in Tyume Valley that are seeking UK partner schools:
<ul>
<li> TVS’ Development Worker will guide Tyume Valley schools wishing to form a school partnership through the completion of any documentation. TVS will support the formulation of a joint partnership agreement that both schools construct together and then sign.
</li>
<li> TVS will help to identify a suitable UK partner school for any school deciding to seek a partnership.
</li>
<li> TVS will support South African schools in obtaining passports and visas for their teachers to visit the UK school.
</li>
<li> TVS will publicise and celebrate the school partnerships through its newsletters and annual reports
</li>
<li> TVS will ensure that partner schools are aware of any child protection/safeguarding policies existing in each country that need to be satisfied in any school visits.
</li>
<li> TVS will seek to evaluate the impact of any school partnership and publish this on its website.
</li>
</ul>
