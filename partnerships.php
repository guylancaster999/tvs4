<h3>What are school links?</h3>
 TVS encourages links between UK and Tyume Valley schools. 
 Formal links – partnerships – between UK and Tyume Valley schools can help raise educational standards in Tyume Valley and promote the professional development of teachers. 
 Educational objectives are central to school linking; any fund-raising activities are secondary.
<br/>
<br/>
We have helped a number of schools to set up partnerships with UK schools and enabled teacher exchanges between those partner schools.

<br/>
<br/>
Partnerships between UK and Tyume schools have many benefits:
<ul>
<li>They enable children in each school to share experiences and learn about life in another country.
</li>
<li>They offer opportunities and funding for teacher exchanges that enhance teachers’ experience and expand their skills.
</li>
<li>They offer a focus for many aspects of the curriculum eg geography, religion, diversity, social justice, peace and conflict.
</li>
<li> They can leave lasting legacies in the lives of children and teachers in both countries.
</li>
</ul>
