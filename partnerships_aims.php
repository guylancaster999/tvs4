<li> Partnership co-ordinators are identified by the Headteacher/Principal in the linked schools.</li>
<li> Once in contact with each other, schools jointly identify their priorities for the partnership and define the educational outcomes they are seeking to achieve.</li>
<li> Regular methods of communication between each school are agreed upon eg texting by mobile phone, Email or Skype.</li>
