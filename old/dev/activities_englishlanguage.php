In 2011 TVS began a programme of English Language training for primary school teachers and children. TVS funds a South African teacher trainer who, together with Vicky Riley, TVS volunteer co-ordinator in Tyume, introduces local teachers to innovative English Language teaching methods in order to improve their skills and confidence in teaching English. Teachers are supported to put their learning into practice back in their own schools and eventually take on mentoring fellow teachers themselves. Currently teachers from 12 schools and over 50 children are involved.
                       <br/>
                       <br/>

In 2016 we asked the teachers in the English Language training programme and some of their colleagues for their views on the programme. All thought the programme benefitted the children, who learnt English faster and were more confident in writing and speaking in English.
