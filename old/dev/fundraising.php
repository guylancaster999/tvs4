<br/>
Diana says:
<blockquote>
I was born in Uganda and the first half of my working life was as a teacher in Botswana and later managing different voluntary sector organisations in West Africa. This has given me valuable insights into both the need for good educational opportunities and the challenges involved in delivering these. Tyume Valley Schools (www.tvschools.org.uk) concentrates on improving children’s fluency and confidence in English (with a focus on teaching the teachers) and introducing basic IT skills. 
TVS is a very small charity, reliant on extensive volunteer effort and has no overheads to suck up resources. So I am confident the pennies raised by my run will go a very long way. To sponsor Diana click <A href="https://mydonate.bt.com/events/tvsgreatnorthrun2016/303643">here</a>. 
