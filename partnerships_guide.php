Tyume Valley Schools work with Tyume's rural schools and provides support in the form of resources and training, supplementary subject tuition and extra-curricular activities. 
Teacher training is an essential element of everything that we do. 
Our volunteers aim to help and support local teachers to learn new methods and develop best practice. 
We have also assisted a number of schools in setting up partnerships with UK schools and enabled teacher exchanges between those partner schools.
<br/>
<br/>
TVS encourages school partnerships as they can support the core aims of the charity, to help raise educational standards in Tyume Valley and support the professional development of teachers. 
These educational objectives are central to school linking; any fund-raising activities are secondary.
  <br/>
                   <h3> Aims and Objectives for School Partnerships</h3>
<ul>
<li>School partnerships should focus on collaborative projects which support the curriculum in both schools (see Appendix 1 for suggested activities)
</li>
<li>In the partnership there is the expectation that young people and educators build awareness of global issues (see Appendix 2)
</li>
<li>Learners will share information about their local context with their partner school.
</li>
<li>Educators/teachers will share information about their educational practice and identify priority areas of practice to improve.
</li>
<li>The partnership will be equitable and aim to meet the identified needs of both partner schools.
</li>
<li>Partnerships are focussed on pupil learning and not on fund-raising activities by one school on behalf of another.</li>
</ul>

<h3>How these can be achieved</h3>
<ul>
<li>A school in Tyume Valley agrees to form a partnership and informs TVS. It identifies what it seeks to gain from its link.
</li>
<li>Tyume Valley Schools helps to identify a suitable UK partner School.
</li>
<li>Partnership co-ordinators are identified by the Headteacher/Principal in the linked schools.
</li>
<li>Once in contact with each other, schools jointly identify their priorities for the partnership and define the educational outcomes they are seeking to achieve.
</li>
<li>Regular methods of communication between each school are agreed upon eg texting by mobile phone, Email or Skype.
</li>
<li>Schools together develop a long term plan and timetable for thei partnership.
</li>
<li>The international partnership becomes part of each school’s Development plan.
</li>
<li>Partner schools run activities which introduce global themes to their pupils (see Appendix 1 and 2 for key themes and activity suggestions)
</li>
<li>Schools mutually develop a Partnership policy to guide their work.
</li>
<li>A partnership agreement is agreed and signed between each school.
</li>
<li>By common agreement one school takes on the role of applying for any grants that will allow reciprocal teacher exchange (See Appendix 3 for expectations and aims of staff exchange.)
</li>
<li>Teacher exchange will be for practising classroom teachers. * Partner schools will be registered on the website www.britishcouncil.org/schoolsonline (This provides useful advice as well opportunities for other finding partners.)
</li>
</ul>
<h3>Appendix 1. Suggestions for joint projects.</h3>
<ul>
<li>Each school produces a poster on what they know about the partners’ country, region, village or country at the outset of the link. Then after researching their partner’s country etc. they produce a second poster. This allows for discussion on stereotypes and realities of initial perceptions.
</li>
<li>Introductory letter exchange- email exchange using school addresses (not personal email of personal address.) Teachers identify buddy partners.
</li>
<li>Pupils produce a PowerPoint introducing themselves and their school to each other. These are then exchanged. This can be on a flash drive or CD if internet link is weak.
</li>
<li>Pupils produce and exchange a weekly diary with illustrations.
</li>
<li>A whole class can produce a hand book about their school and its locality.
</li>
<li>Schools exchange a dozen pictures of their school /locality. Partners then annotate picture asking key questions. Partner school writes answers and returns them to the school generating the questions - either by email or letter.
</li>
<li>Mapping skills- pupils plot and draw their journey to school.
</li>
<li> Both schools log the weather locally and each week gives a summary of key facts gathered to each other.
</li>
<li> Pupils produce simple Xhosa- English phrasebook. With illustrations where necessary.
</li>
<li> Each partner produces a cook book of basic local recipes.
</li>
<li> Bi-lingual story exchange- Learners create stories that can be shared with younger children in both English and Xhosa.
</li>
<li> Partners produce a Guide Book to their own area and exchange them.
</li>
<li> Projects can be developed that link themes from the curriculum of each school- for example; Water and the water cycle. In Maths for example – problem solving.
</li>
</ul>
There are many more suggestions- on the website previously mentioned and the www.britishcouncil.org/connectingclassrooms and Get Global! A skills based approach to active global citizenship- can be downloaded from www.oxfam.org.uk/education
