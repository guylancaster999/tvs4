<h3>Appendix 3. Staff Exchanges</h3>
The expectation is that practising classroom teachers visit partner schools rather than Headteachers or Principals, unless they have a significant teaching role in their schools.
<br/><br/>
Visiting and hosting provide staff with the opportunity to strengthen professional relationships and experience partners’ teaching and learning practices, opportunities and everyday challenges
<br/>
<h3>Expectations around teacher visits to partner schools</h3>
Before the visit:
<ul>
<li> Exchange draft visit plans and objectives for comments and approval
</li>
<li> Teachers familiarise themselves with background information about their partner’s school, country, culture and education system
</li>
<li> Teachers are made aware and agree about how costs (internal travel, accommodation, meals etc.) will be covered during visit. The Co-ordinators need to ensure this is done.
</li>
<li> Hosting teachers need to be aware of dietary or religious requirements, health issues of visitors. Agreement is needed as to whether hotels or staff hosts the visitors?
</li>
<li> Co-ordinators need to have sorted out passports, visas, insurance etc. in good time.
</li>
</ul>
During the visit:
<ul>
<li> Allow opportunities for visitor to be immersed fully in life of the school. Involve Governors, parents and community in the visit through assemblies and a possible social event.
</li>
<li> Give visitors an opportunity to share information about their school.
</li>
<li> Involve visitors in Professional Development or training opportunities.
</li>
<li> Allow time to jointly plan future work and what material, resources will be needed.
</li>
<li> Agree how the joint work will be evaluated.
</li>
<li> Review Partnership agreement and any challenges that have arisen.
</li>
<li> Work jointly on any joint grant application for funding and how you will report back to funders.
</li>
<li> Evaluate work so far.
</li>
<li> Take photos, videos, make recordings, collect artefacts and resources. Involve learners in what to include.
</li>
<li> Celebrate your visit with local press, radio or TV.
</li>
<li> Keep a daily diary of your visit.
</li>
</ul>
After the visit:
<ul>
<li> Write up observations and suggestions for the future
</li>
<li> Note down what you’ve learned
</li>
<li> Note what you want to know more about.
</li>
<li> Follow up agreed action points
</li>
<li> Prepare report for colleagues, governors, parents, community groups through meetings and assemblies.
</li>
</ul>
