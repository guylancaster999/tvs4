                       <h3>Welcome to TVS</h3> 
							Tyume Valley Schools (TVS) is a UK-based charity. 
							Our vision is to improve the lives of children living in the Tyume Valley area of Eastern Cape Province, South Africa. 
							We work with local schools to improve teaching and learning and open doors to future opportunities. 
							Although more than twenty years since the end of apartheid, the legacy lives on in the form of badly maintained schools, lack of resources, poorly trained teachers and low educational achievement. 
                            This disadvantage is increased by the high unemployment and poor infrastructure in the rural Tyume Valley, which is about 120 km from the nearest airport in East London and 60km from King Williams Town, the nearest large town.<br/><br/>
							Click <a href="map.php">here</a> for  a map showing where the project is located. 
							<br/><br/>
							<h3>What do we do?</h3>
							<ul>
							<li>We work with Tyume's rural schools and provide support in the form of resources and training, supplementary subject tuition and extra-curricular activities. Teacher training is an essential element of everything that we do. 
                            Our volunteers aim to help and support local teachers to learn new methods and develop best practice. 
                            We have also assisted a number of schools in setting up partnerships with UK schools and enabled teacher exchanges between those partner schools. 
                            To read about the link between Kwezana Primary School and Settle Primary School (North Yorkshire), click <a href="http://www.settle-pri.n-yorks.sch.uk">here</a>. 
							We have equipped a <A href="activities_resourcecentre.php">Resource centre</a> with books, computers and other resources. 
							<br/><br/>
							</li>
							<li>We provide <a href="activities_it.php" title="IT teaching">IT teaching</a> to over 200 children in 9 primary and 3 secondary schools.<br/>
							<br/>
							</li>
							<li>We run an <a href="activities_englishlanguage.php" title="English language programme in Eastern Cape Schools">English language programme</a> in 8 primary schools, involving 50 children each year.<br/>
<br/>
							</li>
							<li>We run regular <a href="activities_studycamps.php" title="Srtudy Camps in Eastern Cape Schools"> Study camps</a> at nearby <a href="http://www.hobbiton.co.za/">Hobbiton Outdoor Activity Centre</a>, Hogsback. 
                            Some 100 children from the Tyume Valley attend these camps every year.</li>
							</ul>  
                         
