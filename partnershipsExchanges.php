					  Staff exchanges provide opportunities to strengthen professional relationships and to experience teaching and learning practices, opportunities and everyday challenges in the partner school. The expectation is that exchanges take place between practicing teacher rather than headteachers or principals, unless they have a significant teaching role in their schools.
<br/>
<br/>
<strong>Before an exchange visit:</strong>
<ul>
<li>Exchange draft visit plans and objectives for comment and approval
</li>
<li>Teachers should familiarise themselves with background information about their partner’s school, country, culture and education system
</li>
<li>Teachers should be aware of and agree about how costs (internal travel, accommodation, meals etc.) will be covered during visit. The link Co-ordinator in each school needs to ensure this is done.
</li>
<li>Accommodation for visiting teachers needs to be arranged at a local hotel or with school staff.
</li>
<li>Hosting teachers need to be aware of dietary or religious requirements, health issues of visitors.
</li>
<li>Passports, visas, travel insurance etc. need to be arranged in good time.
</li>
</ul>
<strong>During the visit:</strong>
<ul>
<li>Allow opportunities for visitor to be immersed fully in life of the school.
</li>
<li>Involve Governors, parents and community in the visit through assemblies and a possible social event.
</li>
<li>Give visitor an opportunity to share information about their school.
</li>
<li>Involve visitors in professional development or training opportunities.
</li>
<li>Evaluate achievements of the partnership to date.
</li>
<li>Review the partnership agreement and any challenges that have arisen.
</li>
<li>Together plan future work and identify the materials/resources that will be needed.
</li>
<li>Agree how the future joint work will be evaluated.
</li>
<li>Work jointly on any joint grant application for funding and arrangements for reporting back to funders.
</li>
<li>Take photos, videos, make recordings, collect artefacts and resources from the visit. Involve learners in what to include.
</li>
<li>Celebrate the visit with local press, radio or TV.
</li>
<li>Keep a daily diary of your visit.
</li>
</ul>
<strong>After the visit:</strong>
<ul>
<li>Write up observations and suggestions for the future
</li>
<li>Note down what you’ve learned
</li>
<li>Note what you want to know more about.
</li>
<li>Follow up agreed action points
</li>
<li>Prepare report for colleagues, governors, parents, community groups through meetings and assemblies.</li>
</ul>
